export class Dattruoc{
    Mathe: string;
    Masach: string;
    Ngaydattruoc: Date;
    Danhan:boolean;
    Tensach:string
    constructor(
        Mathe: string,
        Masach: string,
        Ngaydattruoc: Date,
        Danhan:boolean,
        Tensach:string ) {
        this.Mathe = Mathe;
        this.Masach = Masach;
        this.Ngaydattruoc = Ngaydattruoc;
        this.Tensach = Tensach;
        this.Danhan = Danhan
    }
}
